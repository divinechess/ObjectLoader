package com.android.objectloader.objectloader;

class Vector3D {

    float x, y, z;

    public Vector3D(float parseFloat, float parseFloat2, float parseFloat3) {
        x = parseFloat;
        y = parseFloat2;
        z = parseFloat3;
    }

    public float getX() {

        return x;
    }

    public float getY() {

        return y;
    }

    public float getZ() {

        return z;
    }



}